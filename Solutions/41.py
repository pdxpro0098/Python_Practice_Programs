def removePunctuations(string):
    table = str.maketrans({":": "", ";": "", ",": "", ".": "", "[": "", "]": "", "(": "", ")": "", "{": "", "}": "", "'": "", "?": "", "/": "", "<": "", ">": "",
                          "+": "", "-": "", "_": "", "=": "", "/": "", "|": "", "!": "", "@": "", "#": "", "$": "", "%": "", "^": "", "&": "", "*": "", "\\": "", "\"": ""})
    return string.translate(table)


